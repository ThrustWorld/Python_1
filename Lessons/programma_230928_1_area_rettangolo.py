# Utilizzo la funzione input per chiedere all'utente il valore di base e altezza
base = int(input("Immettere la base: "))
altezza = int(input("Immettere l'altezza: "))
# Calcolo l'area
area  = base * altezza
# La visualizzo
print("L'area del rettangolo è", area)

